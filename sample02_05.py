# 算术运算符：+（加）、-（减）、*（乘）、/（除）、%（取余）、**（求幂）、//(求整除)
import math
i=7
j=2
print(i+j) # 9
print(i-j) # 5
print(i*j) # 14
print(i/j) # 3.5
print(i%j) # 1
print(i**j) # 49
print(i//j) # 3

#不同数值类型的数值混合运算时，例如：整形和浮点型混合运算，整形转化为浮点型参与运算
print(2*2.5) # 5.0
# 运算时涉及到圆周率和自然对数，可以导入math模块
print(math.pi) # 3.141592653589793
print(math.e) # 2.718281828459045

