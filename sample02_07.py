# 位运算符
# <<:左移运算符
# >>:右移运算符
# &:按位与，每位上都是1，才得到1
# |:按位或，只要有一位上是1.就得到1
# ^:按位异或，相同为0，不同为1
# ~：按位的非，按位取反,注意最高位为符号位

# 2的二进制：0010
print('2 << 3',2 << 3) # 2 << 3 16
print('2 >> 3',2 >> 3) # 2 >> 3 0

# 2的二进制：0010
# 1的二进制：0001
print('2 & 1',2 & 1) # 2 & 1 0
print('2 | 1',2 | 1) # 2 | 1 3
print('2 ^ 1',2 ^ 1) # 2 ^ 1 3

# (补码)
print('~2',~2) # -3
print('~1',~1) # -2
print('~0',~0) # -1

# 所有正整数的按位取反是其本身+1的负数
# 所有负整数的按位取反是其本身+1的绝对值
# 零的按位取反是 -1
