# 递归函数：函数调用自身，需要跳出循环调用的条件

# 需求：使用递归函数计算阶乘
# 思路：n！=n*(n-1)*(n-2)*···*1
#     求n的阶乘可以归结为求n-1的阶乘与n的乘积
#     求n-1的阶乘可以归结为求n-2的阶乘与n-1的乘积
#     求n-2的阶乘可以归结为求n-3的阶乘与n-2的乘积
#     ····
#     求2的阶乘可以归结为求1的阶乘与2的乘积
#     求1的阶乘可以归结为1的阶乘与1的乘积
#     f(n)=1  (n==1)
#     f(n)=f(n-1)*n  (n!==1)

def factorial(n):
    if n!=1:
        return n * factorial(n-1)
    else:
        return 1

print('{0}阶乘为{1}'.format(5,factorial(5))) # 5阶乘为120